const int N=3e5+10;
int n,a[N*2+10];
signed main(){
    std::cin>>n;
    for (int i=1;i<=n;i++){
        std::cin>>a[i];
        a[i+n]=a[i];
    }
    //最小表示法：寻找循环同构串中字典序最小的那一个串
    int i=1,j=2;
    while (i<=n && j<=n){
        int k=0;
        for (k=0;k<n && a[i+k]==a[j+k];k++);
        if (a[i+k]>a[j+k]){
            i=i+k+1;       
        }
        else{
            j=j+k+1;
        }
        if (a[i]==a[j]){
            j++;
        }
    }
    int s=std::min(i,j);
    for (int k=s,len=1;len<=n;len++,k++){
        std::cout<<a[k]<<" ";
    }
    return 0;
}